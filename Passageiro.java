package Trabalho;

public class Passageiro {

    private String nome;
    private String cpf;
    private String fone;

    public String getNome(){
        return this.nome;
    }
    public void setNome(String n){
        this.nome = n;
    }
    public String getCpf(){
        return this.cpf;
    }
    public void setCpf(String cpf){
        this.cpf = cpf;
    }
    public String getFone(){
        return this.fone;
    }
    public void setFone(String f){
        this.fone = f;
    }

}
